UPDATE Material
    SET Material_Code = :Material_Code,
        Material_Name = :Material_Name,
        Material_Mnemonic_Code = :Material_Mnemonic_Code,
        Material_Barcode = :Material_Barcode,
        Material_Description = :Material_Description,
        Specification = :Specification,
        Origin = :Origin,
        Trademark = :Trademark,
        Approval_No = :Approval_No,
        Purchasing_Price = :Purchasing_Price,
        Purchasing_Unit = :Purchasing_Unit,
        Purchasing_Packing_Size = :Purchasing_Packing_Size,
        Input_Tax_Rate = :Input_Tax_Rate,
        Selling_Price = :Selling_Price,
        Selling_Unit = :Selling_Unit,
        Selling_Packing_Size = :Selling_Packing_Size,
        Retail_Price = :Retail_Price,
        Retail_Unit = :Retail_Unit,
        Retail_Packing_Size = :Retail_Packing_Size,
        Output_Tax_Rate = :Output_Tax_Rate,
        Cost_Accounting_Method = :Cost_Accounting_Method,
        Comment = :Comment,
        Last_Modifier_User_Id = :user,
        Last_Modification_Date_Time = CURRENT_TIMESTAMP,
        Version = Version + 1
    WHERE Material_Id = :Original_Material_Id
        AND Version = :Original_Version